package com.jkevinx23.instaapp;

import javax.swing.JLabel;

/**
 *
 * @author jkevi
 */
public class Feed {
    
    private JLabel profilePhoto;
    private JLabel username;
    private JLabel temp;
    private JLabel description;
    private JLabel imageView;

    public JLabel getProfilePhoto() {
        return profilePhoto;
    }

    public void setProfilePhoto(JLabel profilePhoto) {
        this.profilePhoto = profilePhoto;
    }

    public JLabel getUsername() {
        return username;
    }

    public void setUsername(JLabel username) {
        this.username = username;
    }

    public JLabel getTemp() {
        return temp;
    }

    public void setTemp(JLabel temp) {
        this.temp = temp;
    }

    public JLabel getDescription() {
        return description;
    }

    public void setDescription(JLabel description) {
        this.description = description;
    }

    public JLabel getImageView() {
        return imageView;
    }

    public void setImageView(JLabel imageView) {
        this.imageView = imageView;
    }
    
    
    
    
}
